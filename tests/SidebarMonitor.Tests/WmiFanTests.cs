using SidebarMonitor.Etw;
using Xunit;

namespace SidebarMonitor.Tests;

/// <summary>
/// The HP WMI fan-table decode (query 0x2F): a 2-byte header ([0] unknown, [1] entry count) then
/// [cpu, gpu, unknown] triples, each byte *100 = rpm. The reference table below was captured live on
/// a Victus 16-s0xxx (board 8BD4); its top CPU/GPU speeds are the 100% denominators for the fan %.
/// </summary>
public class WmiFanTests
{
    // Full 128-byte reply of the fan-table GET, captured live on the 7840HS Victus 16-s0xxx.
    private static readonly byte[] CapturedTable =
    [
        0x02, 0x2A, 0x00, 0x00, 0x0F, 0x12, 0x15, 0x19, 0x13, 0x16, 0x1A, 0x14, 0x17, 0x1A, 0x15, 0x18,
        0x1B, 0x16, 0x19, 0x1C, 0x17, 0x1A, 0x1E, 0x18, 0x1B, 0x1F, 0x19, 0x1C, 0x20, 0x1A, 0x1D, 0x21,
        0x1B, 0x1E, 0x22, 0x1C, 0x1F, 0x23, 0x1D, 0x20, 0x24, 0x1E, 0x21, 0x24, 0x1F, 0x22, 0x25, 0x20,
        0x23, 0x26, 0x21, 0x24, 0x27, 0x22, 0x25, 0x28, 0x23, 0x26, 0x29, 0x24, 0x27, 0x29, 0x25, 0x28,
        0x29, 0x26, 0x29, 0x29, 0x27, 0x2A, 0x2A, 0x28, 0x2B, 0x2B, 0x29, 0x2C, 0x2D, 0x2A, 0x2D, 0x2E,
        0x2B, 0x2E, 0x2E, 0x2C, 0x2F, 0x2F, 0x2D, 0x30, 0x2F, 0x2E, 0x31, 0x30, 0x2F, 0x32, 0x31, 0x30,
        0x33, 0x31, 0x31, 0x34, 0x31, 0x32, 0x35, 0x32, 0x33, 0x36, 0x33, 0x34, 0x37, 0x33, 0x35, 0x38,
        0x33, 0x36, 0x39, 0x33, 0x37, 0x3A, 0x33, 0x38, 0x3B, 0x33, 0x39, 0x3C, 0x33, 0x3A, 0x3D, 0x34,
    ];

    [Fact]
    public void DecodeTableMax_CapturedVictusTable_TopsAt5800Cpu6100Gpu()
    {
        var (cpu, gpu) = WmiFan.DecodeTableMax(CapturedTable);
        Assert.Equal(5800, cpu);   // 0x3A * 100
        Assert.Equal(6100, gpu);   // 0x3D * 100
    }

    [Fact]
    public void DecodeTableMax_HonoursHeaderEntryCount()
    {
        // Header says 2 entries, so bytes past the second triple must be ignored.
        byte[] t = [0x00, 0x02, /*e0*/ 0x0A, 0x14, 0x00, /*e1*/ 0x1E, 0x28, 0x00, /*ignored*/ 0x63, 0x63, 0x63];
        var (cpu, gpu) = WmiFan.DecodeTableMax(t);
        Assert.Equal(3000, cpu);   // 0x1E * 100, not 0x63
        Assert.Equal(4000, gpu);   // 0x28 * 100
    }

    [Fact]
    public void DecodeTableMax_ShortBuffer_ReturnsZero()
    {
        Assert.Equal((0, 0), WmiFan.DecodeTableMax([0x02, 0x2A, 0x00, 0x00]));
    }
}
